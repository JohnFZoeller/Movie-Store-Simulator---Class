#include "movie.h"

// ------------------------------------constructor----------------------------------------------- 
// Description: sets year, director and title to default
// -------------------------------------------------------------------------------------------------------------
Movie::Movie()
{
    year = 1900;
    director = "";
    title = "";
}

// ------------------------------------getTitle----------------------------------------------- 
// Description: returns title
// -------------------------------------------------------------------------------------------------------------
string Movie::getTitle() const{
    return title;
}

// ------------------------------------getDirector----------------------------------------------- 
// Description: returns director
// -------------------------------------------------------------------------------------------------------------
string Movie::getDirector() const
{
    return director;
}

// ------------------------------------getYear----------------------------------------------- 
// Description: returns year
// -------------------------------------------------------------------------------------------------------------
int Movie::getYear() const
{
    return year;
}

// ------------------------------------getStock----------------------------------------------- 
// Description: returns stock
// -------------------------------------------------------------------------------------------------------------
int Movie::getStock() const
{
    return stock;
}

// ------------------------------------setSrock----------------------------------------------- 
// Description: sets stock to user value
// -------------------------------------------------------------------------------------------------------------
bool Movie::setStock(int s)
{
    stock = s;
    if(stock < 0)
        return false;
    else return true;
}

// ------------------------------------setTitle----------------------------------------------- 
// Description: sets title to user value
// -------------------------------------------------------------------------------------------------------------
bool Movie::setTitle(string t)
{
    title = t;
    if(title.empty())
        return false;
    else return true;
}

// ------------------------------------set director----------------------------------------------- 
// Description: sets director to user value
// -------------------------------------------------------------------------------------------------------------
bool Movie::setDirector(string d)
{
    director = d;
    if(director.empty())
        return false;
    else return true;
}

// ------------------------------------setYear----------------------------------------------- 
// Description: sets year to default valur
// -------------------------------------------------------------------------------------------------------------
bool Movie::setYear(int y)
{
    year = y;
    if(year == 1900)
        return false;
    else return true;
}